

export function add(value1, value2) {
    return Number(value1) + Number(value2);
}

export function subtract(value3, value4) {
    return Number(value3) - Number(value4);
}

export function multiply(value5, value6) {
    return Number(value5) * Number(value6);
}

export function divide(value7, value8) {
    return Number(value7) / Number(value8);
}







